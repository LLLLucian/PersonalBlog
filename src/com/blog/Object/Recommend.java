package com.blog.Object;

public class Recommend {

	private int RecommendId;
	private String RecommendTitle;
	private String RecommendIntro;
	private String RecommendLocation;
	
	public int getRecommendId() {
		return RecommendId;
	}
	public void setRecommendId(int recommendId) {
		RecommendId = recommendId;
	}
	public String getRecommendTitle() {
		return RecommendTitle;
	}
	public void setRecommendTitle(String recommendTitle) {
		RecommendTitle = recommendTitle;
	}
	public String getRecommendIntro() {
		return RecommendIntro;
	}
	public void setRecommendIntro(String recommendIntro) {
		RecommendIntro = recommendIntro;
	}
	public String getRecommendLocation() {
		return RecommendLocation;
	}
	public void setRecommendLocation(String recommendLocation) {
		RecommendLocation = recommendLocation;
	}
	
	
}
